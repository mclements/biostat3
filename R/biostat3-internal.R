year <- function(date, trunc=FALSE, year.length=365.24) {
    stopifnot(class(date)=="Date")
    year <- as.numeric(format(date,"%Y"))
    if (trunc) return(year)
    start <- as.Date(sprintf("%i-01-01",year))
    year + as.numeric(date-start)/year.length
}

